let lastColor = "white"; // Varsayılan olarak beyaz
let lastBackgroundColor = "white"; // Varsayılan olarak siyah

$(document).ready(function() {
  $(".project-card").click(function() {
    
    $(".project-card").css("transform", "scale(1)");
    $(this).css("transform", "scale(1.1)");

    const projectKey = $(this).data("project");
    const project = projectDetails[projectKey];


  
    let buttonsHtml = '';
    if (project.googlePlay !== '#') buttonsHtml += `<a href="${project.googlePlay}" class="btn btn-primary">Google Play</a>`;
    if (project.steam !== '#') buttonsHtml += `<a href="${project.steam}" class="btn btn-secondary">Steam</a>`;
    if (project.itch !== '#') buttonsHtml += `<a href="${project.itch}" class="btn btn-info">Itch.io</a>`;
    if (project.webGL !== '#') buttonsHtml += `<a href="${project.webGL}" class="btn btn-success">Play</a>`;
    if (project.youtube !== '#') buttonsHtml += `<a href="${project.youtube}" class="btn btn-danger">YouTube</a>`;

    const projectDetailsHtml = `
      <div class="card">
        <div class="card-header">
          <h3>${project.name}</h3>
        </div>
        <div class="card-body">
          <div class="row">
            <div class="col-md-6">
              <img src="${project.image}" alt="${project.name}" class="img-fluid">
            </div>
            <div class="col-md-6">
              <p>${project.description}</p>
              <div class="btn-group" role="group" aria-label="Project Links">
                ${buttonsHtml}
              </div>
            </div>
          </div>
          <div class="row mt-4">
            <div class="col-md-6">
              <img src="${project.additionalImages[0]}" alt="Additional Image 1" class="img-fluid">
            </div>
            <div class="col-md-6">
              <img src="${project.additionalImages[1]}" alt="Additional Image 2" class="img-fluid">
            </div>
          </div>
        </div>
      </div>
    `;

    

    $("#project-details").html(projectDetailsHtml);

     
   

  // CSS'i güncelle
  $("#project-details .card-body").css({
    "color": lastColor,
    "background-color": lastBackgroundColor,
    "transition": "background-color 0.5s ease, color 0.5s ease"
  });

    // Önce transition özelliğini ayarla
$("#project-details .card-body").css({
  "transition": "background-color 0.5s ease, color 0.5s ease"
});



  // Biraz bekleyip sonra renkleri değiştir
  setTimeout(function() {
    $("#project-details .card-body").css({
      "color": project.color,
      "background-color": project.backgroundColor
    });
  }, 50); 
    
    // Son renkleri güncelle
    lastColor = project.color;
    lastBackgroundColor = project.backgroundColor;
  });

  
});

  
  
  


  