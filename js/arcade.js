function initWebGL() {
  const canvas = document.getElementById('bg-canvas');
  
  // Sahne oluÅŸtur
  scene = new THREE.Scene();
  
  // Kamera oluÅŸtur
  camera = new THREE.PerspectiveCamera(75, window.innerWidth / window.innerHeight, 0.1, 1000);
  camera.position.z = 50;
  
  // Renderer oluÅŸtur
  renderer = new THREE.WebGLRenderer({ 
    canvas: canvas, 
    antialias: true,
    alpha: true 
  });
  renderer.setSize(window.innerWidth, window.innerHeight);
  renderer.setPixelRatio(window.devicePixelRatio);
  
  // ParÃ§acÄ±klar oluÅŸtur
  const particlesGeometry = new THREE.BufferGeometry();
  const particleCount = 300; // Performans iÃ§in sayÄ±yÄ± ayarlayabilirsiniz
  
  const positionArray = new Float32Array(particleCount * 3);
  const colorArray = new Float32Array(particleCount * 3);
  
  // WebGL arka plan iÃ§in parÃ§acÄ±k renkleri gÃ¼ncelleme
for (let i = 0; i < particleCount * 3; i += 3) {
  // Konumlar
  positionArray[i] = (Math.random() - 0.5) * 100;
  positionArray[i + 1] = (Math.random() - 0.5) * 100;
  positionArray[i + 2] = (Math.random() - 0.5) * 100;
  
  // Logo renklerine uygun parÃ§acÄ±klar (turuncu ve yeÅŸil)
  const colorType = Math.random();
  if (colorType < 0.5) {
    // Turuncu/kÄ±rmÄ±zÄ± ton
    colorArray[i] = Math.random() * 0.6 + 0.4; // R
    colorArray[i + 1] = Math.random() * 0.3; // G
    colorArray[i + 2] = Math.random() * 0.1; // B
  } else {
    // YeÅŸil/turkuaz ton
    colorArray[i] = Math.random() * 0.1; // R
    colorArray[i + 1] = Math.random() * 0.6 + 0.4; // G
    colorArray[i + 2] = Math.random() * 0.4; // B
  }
}
  
  particlesGeometry.setAttribute('position', new THREE.BufferAttribute(positionArray, 3));
  particlesGeometry.setAttribute('color', new THREE.BufferAttribute(colorArray, 3));
  
  const particlesMaterial = new THREE.PointsMaterial({
    size: 0.3,
    transparent: true,
    opacity: 0.5,
    vertexColors: true,
    blending: THREE.AdditiveBlending
  });
  
  particleSystem = new THREE.Points(particlesGeometry, particlesMaterial);
  scene.add(particleSystem);
  
  // Pencere boyutu deÄŸiÅŸiklikleri iÃ§in listener
  window.addEventListener('resize', onWindowResize);
  
  // Animasyonu baÅŸlat
  animate();
}

function onWindowResize() {
  camera.aspect = window.innerWidth / window.innerHeight;
  camera.updateProjectionMatrix();
  renderer.setSize(window.innerWidth, window.innerHeight);
}

function animate() {
  requestAnimationFrame(animate);
  
  // ParÃ§acÄ±klarÄ± dÃ¶ndÃ¼r
  particleSystem.rotation.x += 0.0003;
  particleSystem.rotation.y += 0.0003;
  
  // Sahneyi render et
  renderer.render(scene, camera);
}

